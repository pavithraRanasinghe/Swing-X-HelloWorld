    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String id=idText.getText();
        String SQL="Select * From Customer where id=?";
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ThogaKade", "root", "ijse");
            PreparedStatement stm = connection.prepareStatement(SQL);
            stm.setObject(1, id);
            ResultSet rst = stm.executeQuery();
            if(rst.next()){
                nameText.setText(rst.getString("name"));                
                addressText.setText(rst.getString("address"));                
                salaryText.setText(rst.getString("salary"));                
            }else{
                JOptionPane.showMessageDialog(this, "No Customer found..");   
            }
                    
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver Not found");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
                
    }//GEN-LAST:event_searchButtonActionPerformed

