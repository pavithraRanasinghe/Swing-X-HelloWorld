    private void codeTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_codeTextActionPerformed
        String code=codeText.getText();
        String SQL="Select * From item where code=?";
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ThogaKade", "root", "1234");
            PreparedStatement stm = connection.prepareStatement(SQL);
           // Object code = null;
            stm.setObject(1, code);
            ResultSet rst = stm.executeQuery();
            if(rst.next()){
                desText.setText(rst.getString("description"));                
                unitPriceText.setText(rst.getString("unitPrice"));                
                qtyText.setText(rst.getString("qtyOnHand"));                
            }else{
                JOptionPane.showMessageDialog(this, "No Item found..");   
            }
                    
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver Not found");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_codeTextActionPerformed

