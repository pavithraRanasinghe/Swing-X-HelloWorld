    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String SQL="Update item set description=?, quantity=?, unitPrice=? where code=?";
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ThogaKade", "root", "1234");
            PreparedStatement stm = connection.prepareStatement(SQL);
            stm.setObject(1, desText.getText() );
            stm.setObject(2, unitPriceText.getText() );
            stm.setObject(3, qtyText.getText() );
            stm.setObject(4, codeText.getText() );
            int res = stm.executeUpdate();
            if(res>0){
                JOptionPane.showMessageDialog(this, "Update Success");   
            }else{
                JOptionPane.showMessageDialog(this, "Update fail");   
            }
                    
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver Not found");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
                
    }//GEN-LAST:event_updateButtonActionPerformed

